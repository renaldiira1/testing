    private void hapusAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusAllActionPerformed
        // TODO add your handling code here:

        //buat model baru
        DefaultTableModel model = new DefaultTableModel();

        //buat object array berisi 4
        Object[] columsName = new Object[4];

        columsName[0] = "ID";
        columsName[1] = "Nama";
        columsName[2] = "Alamat";
        columsName[3] = "Jenis Kelamin";

        model.setColumnIdentifiers(columsName);
        tableData.setModel(model);

    }//GEN-LAST:event_hapusAllActionPerformed

