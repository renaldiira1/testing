    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:

        if (textNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Klom Nama tidak boleh kosong");
        } else if (textAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kolom Alamat tidak boleh kosong");
        } else if (cmbJK.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Kolom Jenis Kelamin tidak boleh kosong");
        } else {
            inputData();
        }
    }//GEN-LAST:event_btnInputActionPerformed

