    public void inputData() {
        //mengambil model dari tabel yang ada
        DefaultTableModel ambilModel = (DefaultTableModel) tableData.getModel();

        int a = ambilModel.getRowCount();
        String id = Integer.toString(a + 1);
        ambilModel.addRow(new Object[]{
            id, textNama.getText(),
            textAlamat.getText(), cmbJK.getSelectedItem().toString()
        });

        textNama.setText("");
        textAlamat.setText("");
        cmbJK.setSelectedIndex(-1);

    }

